vim.api.nvim_create_autocmd({ "TextYankPost" }, {
	pattern = { "*" },
	callback = function()
		vim.highlight.on_yank({
			timeout = 300,
		})
	end,
})



-- keybindings
local opt = { noremap = true, silent = true }
vim.keymap.set("n", "<C-l>", "<C-w>l", opt)
